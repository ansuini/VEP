function area = get_area_from_code(code)

switch code
    
   case 0
       
        area = 'V1b';
        
   case 1
       
        area = 'LM';
        
        
   case 2
        
        area = 'AL';
        
   case 3
       
        area = 'LI';
           
   case 4
        
        area = 'LL';
        
   case 5
        
        area = 'LLb';
        
    
   case 6
       
        area = 'AM';
        
        
    case 7
        
        area = 'LM?';
        
    
      
end


end

